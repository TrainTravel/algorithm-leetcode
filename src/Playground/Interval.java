package Playground;

/**
 * Definition for an Interval.
 *
 * @author BorisMirage
 * Time: 2019/12/02 18:57
 * Created with IntelliJ IDEA
 */
class Interval {
    public int start;
    public int end;

    /**
     * Constructor of interval.
     */
    public Interval() {
    }

    /**
     * Constructor of new interval.
     *
     * @param _start start of interval
     * @param _end   end of interval
     */
    public Interval(int _start, int _end) {
        start = _start;
        end = _end;
    }
}

